package com.cst8334.solitaire.utils;

/**
 * The {@code Callback} interface represents a callback function.
 * It is used to pass a function as an argument to another function.
 * 
 * @version 1.0
 * @since 17
 * @author Austin Kirby
 */
public interface Callback {

  /**
   * Calls the callback function.
   */
  void call();
}
